package ex_04_Operators;

public class Lab037_Operators_Arithmetic_Operators {
    public static void main(String[] args) {

        // Arithmetic Operators
        //        + (Addition)
//                - (Subtraction)
//                * (Multiplication)
//                / (Division)
//                % (Modulo) | Modulus - Remainder

        int a = 20;
        int b = 3;
        float c = 3f;
//If both operants 20, 3 are int then op will also come in int not in decimal.
//If a,b is flot the store will different or if int the store is different.
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); // 6
        System.out.println(a/c); // 6.6666665




    }
}
