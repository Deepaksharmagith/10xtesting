package ex_10_For_Loop;

public class Lab102 {
    public static void main(String[] args) {
        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation(Incre/decre)
        // ICU

        for (int i = 0; i < 10; i++) {
            System.out.println(i);  // use debuging
            // 0,1,2......9 - times it got executed - program 10 times it has executed( 0 to 9)
        }
    }
}
